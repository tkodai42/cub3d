void	set_bonus_tex_7(t_player *p)
{
	if (SKYBOX_SAND)
	{
		set_new_tex_structure(p, "./texture/skybox-sand-N.xpm", TEX_SKYBOX_N);
		set_new_tex_structure(p, "./texture/skybox-sand-E.xpm", TEX_SKYBOX_E);
		set_new_tex_structure(p, "./texture/skybox-sand-W.xpm", TEX_SKYBOX_W);
		set_new_tex_structure(p, "./texture/skybox-sand-S.xpm", TEX_SKYBOX_S);
	}
	else
	{
		set_new_tex_structure(p, "./texture/space.xpm", TEX_SKYBOX_S);
		set_new_tex_structure(p, "./texture/space.xpm", TEX_SKYBOX_E);
		set_new_tex_structure(p, "./texture/space.xpm", TEX_SKYBOX_W);
		set_new_tex_structure(p, "./texture/deathstar1.xpm", TEX_SKYBOX_N);
	}
	set_bonus_tex_8(p);
}

